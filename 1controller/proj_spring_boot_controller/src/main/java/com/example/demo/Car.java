package com.example.demo;

public class Car {
	
	//instance variables
	private String brand;
	private int year;
	
	//autogerated everything 
	public Car(String brand, int year) {
		super();
		this.brand = brand;
		this.year = year;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	




}
